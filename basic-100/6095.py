n = int(input())
count = 0
baduk_board = [[0]*19 for _ in range(19)]

while count < n:
    x, y = map(int, input().split())
    baduk_board[x-1][y-1] = 1
    count += 1

for x in range(19):
    for y in range(19):
        print(baduk_board[x][y], end=' ')
    print()